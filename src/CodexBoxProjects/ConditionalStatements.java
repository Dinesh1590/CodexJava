package CodexBoxProjects;

public class ConditionalStatements {
    public static void main(String[] args) {

        int s=10;

        switch (s)

        {
            case 12 : {
                System.out.println();
            }
            case 'v' :
            {
                System.out.println();



            }
            case 1:{
                System.out.println();
            }
            case 10 :
            {
                System.out.println("its me");
                break;
            }
            default:
            {
                System.out.println("default mee");
            }

        }

    }
}
